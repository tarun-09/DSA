package Graph.DetectCycleinUndirectedGraph;

import java.util.ArrayList;

public class ConstructAdj {
    public static ArrayList<ArrayList<Integer>> contructAdj(int V, int[][] edges) {
        ArrayList<ArrayList<Integer>> adj = new ArrayList<>();

        for (int i = 0; i < V; i++) {
            adj.add(new ArrayList<>());
        }

        for (int[] edge : edges) {
            int u = edge[0];
            int v = edge[1];

            adj.get(u).add(v);
            adj.get(v).add(u);
        }

        return adj;
    }
}
